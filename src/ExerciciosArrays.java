
public class ExerciciosArrays {

	public static void main(String[] args) {

		String [] nomes = { "Josiane", "Juliana", "Henrique", "Julia"};
		
		System.out.println(nomes [0] + "\n" + nomes [1] + "\n" + nomes [2] + "\n" + nomes [3]);
		
		nomes = new String[] { "Josiane", "Pedro", "Henrique", "Maria"};
		
		System.out.println(nomes [0] + "\n" + nomes [1] + "\n" + nomes [2] + "\n" + nomes [3]);
		
	}

}
